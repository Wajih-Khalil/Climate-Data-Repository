-- 9. Summary (Advanced Materialized Views)
CREATE MATERIALIZED VIEW mv_annual_summary
BUILD IMMEDIATE
REFRESH FAST ON COMMIT
AS
SELECT station_id,
       EXTRACT(YEAR FROM reading_date) as year,
       AVG(temperature_c) as avg_annual_temp,
       MAX(temperature_c) as max_temp,
       MIN(temperature_c) as min_temp
FROM weather_readings
GROUP BY station_id, EXTRACT(YEAR FROM reading_date);

